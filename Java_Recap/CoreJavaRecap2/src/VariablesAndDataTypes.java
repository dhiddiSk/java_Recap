
public class VariablesAndDataTypes {

	public static void main(String[] args) {
		// print variables defined from different data types.
		
		String name = "saikrishna";
		int age = 28;
		double accountNumber = 1234567890;
		boolean isAlive = true;
		float height = 5.10f;
		char shortInitial = 'D';
		
		System.out.println("The name of this person is "+name+" ,his age is "+age+ " , his account number is "+accountNumber+" , he is also alive "+ isAlive+" his current height is "+ height+" his name initial is "+shortInitial);
		

	}

}
